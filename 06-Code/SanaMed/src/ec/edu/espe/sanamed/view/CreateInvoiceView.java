    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String name = txtName.getText().trim();
        String address = txtAddress.getText().trim();
        String branchOffice = txtBranchOffice.getText().trim();
        String email = txtEmail.getText().trim();
        String phone = txtPhone.getText().trim();
        
        boolean isEndConsumer = chbxEndConsumer.isSelected();
        Customer customer = null;
        
        if (!isEndConsumer) {
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(null, "El nombre es necesario");
                return;
            }
            
            if (address.isEmpty()) {
                JOptionPane.showMessageDialog(null, "La direccion es necesaria");
                return;
            }
            
            if (branchOffice.isEmpty()) {
                JOptionPane.showMessageDialog(null, "La sucursal es necesaria");
                return;
            }
            
            if (email.isEmpty()) {
                JOptionPane.showMessageDialog(null, "El email es necesario");
                return;
            }
            
            Pattern emailRegex = Pattern.compile(
                "^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$", Pattern.CASE_INSENSITIVE);
        
            if (!emailRegex.matcher(email).find()) {
                JOptionPane.showMessageDialog(null, "El email es incorrecto");
                return;
            }
            
            if (phone.isEmpty()) {
                JOptionPane.showMessageDialog(null, "El telefono es necesario");
                return;
            }
            
            customer = new Customer(name, address, email, branchOffice, phone);
        }
        
        Invoice invoice = new Invoice(customer);
        invoice.setItems(items);
        
        controller.saveInvoice(invoice);
        JOptionPane.showMessageDialog(null, "Factura guardada exitosamente");
        
        items.clear();
        updateTable();
        
        txtName.setText("");
        txtAddress.setText("");
        txtBranchOffice.setText("");
        txtEmail.setText("");
        txtPhone.setText("");
        spinnerAmount.setValue(1);
        cmbxProducts.setSelectedIndex(-1);
    }//GEN-LAST:event_btnSaveActionPerformed

    private void updateTable() {
        DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
        model.setRowCount(0);
        
        for (InvoiceItem item : items) {
            double totalPrice = item.getProduct().getPrice() * item.getTotal();
            
            model.addRow(new Object[]{
                item.getProduct().getName(),
                item.getTotal(),
                item.getProduct().getPrice(),
                totalPrice
            });
        }
    }

