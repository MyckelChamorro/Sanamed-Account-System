    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        if (cmbxProducts.getSelectedIndex() == -1) return;
        
        Product product = ((ProductItemComboBox) cmbxProducts.getSelectedItem()).getProduct();
        int total = (int) spinnerAmount.getValue();
        boolean found = false;
        
        for (InvoiceItem item : items) {
            if (item.getProduct() == product) {
                item.setTotal(item.getTotal() + total);
                found = true;
            }
        }
        
        if (!found) {
            items.add(new InvoiceItem(product, total));
        }
        
        updateTable();
    }//GEN-LAST:event_btnAddProductActionPerformed

