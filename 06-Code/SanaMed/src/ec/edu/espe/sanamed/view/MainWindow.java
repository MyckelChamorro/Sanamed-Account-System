    public void displayView(String name) {
        if (!views.containsKey(name)) {
            return;
        }
        
        View view = views.get(name);
        view.setOpaque(false);
        
        container.removeAll();
        container.add(view);
        container.revalidate();
        container.repaint();
        view.onReveal();
        MenuItemSelect(name, null);
    }

