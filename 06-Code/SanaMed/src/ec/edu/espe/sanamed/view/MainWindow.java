    private void MenuItemSelect(String name, JButton button) {
        if (name == null || !views.containsKey(name)) {
            return;
        }
        
        for (Component c : menu.getComponents()) {
            if (!(c instanceof JPanel)) {
                continue;
            }
            
            JPanel panel = (JPanel)c;
            JButton item = null;
            
            for (Component c2 : panel.getComponents()) {
                if (c2 instanceof JButton) {
                    item = (JButton) c2;
                    break;
                }
            }
            
            if (item == null) {
                continue;
            }
            
            if (button == null && item.getName().equalsIgnoreCase(name)) {
                button = item;
            }
            
            panel.setOpaque(false);
        }
        
        menu.repaint();
        menu.revalidate();
        
        JPanel panel = (JPanel)button.getParent();
        panel.setOpaque(true);
    }
    
    private void menuItemSelected(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuItemSelected
        if (!(evt.getSource() instanceof JButton)) {
            return;
        }
        
        JButton button = (JButton)evt.getSource();
        String name = button.getName();
        displayView(name);
        MenuItemSelect(name, button);
    }//GEN-LAST:event_menuItemSelected

